# initial value is 0x00
def joycon_crc(data):
  value = 0x00
  for byte in data:
    value = joycon_crc_lookup[value & 0xFF ^ byte & 0xFF]
  return value

def check_packet(packet):
  header = packet[:4]
  length = packet[3:4]
  data1 = packet[4:11]
  data1_crc = packet[11:12]
  data2 = packet[12:]
  print("packet: 0x" + hex_string(packet))
  print("  header: 0x" + hex_string(header))
  print("    length: 0x" + hex_string(length))
  print("  data1: 0x" + hex_string(data1))
  print("  data1_crc: 0x" + hex_string(data1_crc))
  print("    calculated crc: 0x" + hex_string(joycon_crc(data1))) 
  if data2:
    print("  data2: 0x" + hex_string(data2))
  print("")

def hex_string(data):
  if (isinstance(data, int)):
    return format(data, '02X')
  return ", 0x".join(format(x, '02X') for x in data)

